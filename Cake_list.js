function loadCakeList(type) {
    const cakeListContainer = document.querySelector('.cake-grid');
    cakeListContainer.innerHTML = ''; // Clear existing items

    const cakes = CakeProducts[type];
    if (!cakes) {
        console.error('Cake type not found');
        return;
    }

    

    cakes.forEach(cake => {
        const cakeItem = document.createElement('div');
        cakeItem.className = 'cake-item';    
        cakeItem.innerHTML = `
            <a href="cake_template.html?productId=${cake.id}" class="cake-link">
                <div class="cake-info">
                    <h3>${cake.title}</h3>
                    <p>$${cake.price.toFixed(2)}</p>
                </div>
                
                 <img src="${cake.image}" alt="${cake.title}">    
            </a>
        `;
        cakeListContainer.appendChild(cakeItem);

                                             
    });
}

